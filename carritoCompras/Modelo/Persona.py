class Persona:
    def __init__(self,name,edad,genero,dni,tel,mail) -> None:
        self.name = name
        self.edad = edad
        self.genero = genero
        self.dni = dni
        self.tel = tel
        self.mail = mail
    
    def get_name(self):
        return self.name
    
    def get_edad(self):
        return self.name
    
    def get_genero(self):
        return self.name
    
    def get_dni(self):
        return self.name
    
    def get_tel(self):
        return self.name
    
    def get_mail(self):
        return self.name
    
    def __str__(self):
        return self.name
